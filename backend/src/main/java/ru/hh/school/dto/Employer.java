package ru.hh.school.dto;

public class Employer {

    private Integer id;
    private String name;

    public Employer() {
    }

    public Employer(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
